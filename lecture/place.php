	 <script>
        const bottomSheet = document.getElementById('bottomSheet');
        const dragBar = document.getElementById('dragBar');

        let startY = 0;
        let translateY = 0; // 현재 위치
        let isDragging = false;

        const startDrag = (e) => {
            isDragging = true;
            startY = e.touches ? e.touches[0].clientY : e.clientY;
            bottomSheet.style.transition = "none"; // 드래그 중에는 transition 제거
        };

        const onDrag = (e) => {
            if (!isDragging) return;

            const clientY = e.touches ? e.touches[0].clientY : e.clientY;
            const deltaY = startY - clientY; // 움직인 거리 계산
            requestAnimationFrame(() => {
                const screenHeight = window.innerHeight;
                let newTranslateY = translateY - deltaY;

                // 제한된 범위 내에서만 이동
                newTranslateY = Math.max(-screenHeight * 0.55, Math.min(0, newTranslateY));
                bottomSheet.style.transform = `translateY(${newTranslateY}px)`;
            });
        };

        const endDrag = () => {
            isDragging = false;
            bottomSheet.style.transition = "transform 0.3s ease"; // 드래그 해제 시 부드러운 애니메이션

            // 끝난 위치에서 가까운 상태로 자동 정렬
            requestAnimationFrame(() => {
                const screenHeight = window.innerHeight;
                const currentTransform = parseFloat(bottomSheet.style.transform.replace('translateY(', '').replace('px)', '')) || 0;

                if (currentTransform < -screenHeight * 0.35) {
                    bottomSheet.style.transform = `translateY(-${screenHeight * 0.55}px)`; // 위로 이동
                    translateY = -screenHeight * 0.55;
                } else {
                    bottomSheet.style.transform = `translateY(0)`; // 아래로 이동
                    translateY = 0;
                }
            });
        };

        // 터치 및 마우스 이벤트 추가
        dragBar.addEventListener('mousedown', startDrag);
        dragBar.addEventListener('touchstart', startDrag);

        window.addEventListener('mousemove', onDrag);
        window.addEventListener('touchmove', onDrag);

        window.addEventListener('mouseup', endDrag);
        window.addEventListener('touchend', endDrag);
    </script> 
